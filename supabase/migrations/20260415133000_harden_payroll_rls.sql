-- Harden RLS for payroll and related HR-finance tables.
-- Removes broad/unsafe policies and replaces with admin-only policies.

-- ---------------------------------------------------------------------------
-- Helper: identify payroll admins from JWT claims
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.is_payroll_admin()
RETURNS BOOLEAN
LANGUAGE sql
STABLE
AS $$
  SELECT
    COALESCE(auth.role() = 'service_role', FALSE)
    OR COALESCE(auth.jwt()->>'role' IN ('admin', 'payroll_admin', 'payroll_manager'), FALSE)
    OR COALESCE(auth.jwt()->'app_metadata'->>'role' IN ('admin', 'payroll_admin', 'payroll_manager'), FALSE)
    OR EXISTS (
      SELECT 1
      FROM jsonb_array_elements_text(COALESCE(auth.jwt()->'app_metadata'->'roles', '[]'::jsonb)) AS r(role_name)
      WHERE r.role_name IN ('admin', 'payroll_admin', 'payroll_manager')
    );
$$;

-- ---------------------------------------------------------------------------
-- Remove previous broad payroll policies generated by baseline migration
-- ---------------------------------------------------------------------------
DO $$
DECLARE
  t RECORD;
BEGIN
  FOR t IN
    SELECT tablename
    FROM pg_tables
    WHERE schemaname = 'public'
      AND tablename IN (
        'payroll_periods','payroll_runs','payroll_run_employees','payroll_run_lines',
        'leave_credit_policies','leave_credit_ledger','leave_balance_yearly',
        'overtime_entries','overtime_overrides',
        'deduction_catalog','employee_recurring_deductions','employee_one_time_deductions',
        'loan_accounts','loan_installments','loan_transactions',
        'payroll_adjustments','payroll_exceptions','manual_override_audit','payslip_snapshots',
        'payroll_records','payroll_deduction_settings'
      )
  LOOP
    -- Policy name used in 20260415132500_add_payroll_helpers_and_rls_baseline.sql
    EXECUTE format('DROP POLICY IF EXISTS %I ON public.%I;', 'auth_full_' || t.tablename, t.tablename);
    -- Any legacy broad policy accidentally created elsewhere
    EXECUTE format('DROP POLICY IF EXISTS %I ON public.%I;', 'Enable all for authenticated users', t.tablename);
    EXECUTE format('DROP POLICY IF EXISTS %I ON public.%I;', 'Enable all for anon', t.tablename);
  END LOOP;
END $$;

