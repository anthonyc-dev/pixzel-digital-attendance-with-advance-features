-- ---------------------------------------------------------------------------
-- Create strict admin-only policies for payroll domain
-- ---------------------------------------------------------------------------
DO $$
DECLARE
  t RECORD;
BEGIN
  FOR t IN
    SELECT tablename
    FROM pg_tables
    WHERE schemaname = 'public'
      AND tablename IN (
        'payroll_periods','payroll_runs','payroll_run_employees','payroll_run_lines',
        'leave_credit_policies','leave_credit_ledger','leave_balance_yearly',
        'overtime_entries','overtime_overrides',
        'deduction_catalog','employee_recurring_deductions','employee_one_time_deductions',
        'loan_accounts','loan_installments','loan_transactions',
        'payroll_adjustments','payroll_exceptions','manual_override_audit','payslip_snapshots',
        'payroll_records','payroll_deduction_settings','leave_requests'
      )
  LOOP
    EXECUTE format(
      'CREATE POLICY %I ON public.%I FOR ALL TO authenticated USING (public.is_payroll_admin()) WITH CHECK (public.is_payroll_admin());',
      'payroll_admin_only_' || t.tablename,
      t.tablename
    );
  END LOOP;
END $$;
